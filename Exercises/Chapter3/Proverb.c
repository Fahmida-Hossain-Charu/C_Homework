#include <stdio.h>
int main()
{
    int n;
    printf("Enter the no of proverb: ");
    scanf("%d", &n);
    if (n == 1)
        printf("Actions speak louder than words");
    if (n == 2)
        printf("Every cloud has a silver lining");
    if (n == 3)
        printf("Don't count your chickens before they hatch");
    if (n == 4)
        printf("A penny saved is a penny earned");
    if (n == 5)
        printf("All that glitters is not gold");
    return 0;
}